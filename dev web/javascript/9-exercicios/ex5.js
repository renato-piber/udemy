function maiorOuIgual(num1, num2) {
    if (num1 === num2) 
        console.log(`os numeros ${num1} e ${num2} são ingual`);
    else if (num1 > num2)
        console.log(`o numero ${num1} é maior que o numero ${num2}`);
    else
        console.log(`o numero ${num2} é maior que o numero ${num1}`);

}

maiorOuIgual(1,1)